public class e57_Squarerootconvergents_3 {

    public static void main(String[] args){
        int num=89;
        int count=14;
        final int MAX=10000000;
        while (num < MAX){
            num+=199;
            count+=32;
        }
        System.out.println(num+" "+count);

    }
}

